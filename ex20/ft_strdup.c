#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*cpy;
	int		size;
	int		i;

	size = 0;
	while (src[size])
		size++;
	cpy = (char *) malloc(size + 1);
	i = 0;
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
