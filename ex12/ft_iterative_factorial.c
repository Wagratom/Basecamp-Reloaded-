int	ft_iterative_factorial(int nb)
{
	int	result;

	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0)
		return (1);
	result = 1;
	nb++;
	while (--nb > 0)
		result *= nb;
	return (result);
}
